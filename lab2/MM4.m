function [X] = MM4(A, B)  
  B = [
    B(1)
    B(2) - B(1) * A(2, 1) / A(1, 1)
    B(3)
  ]
  
  A = [
    A(1, 1) A(1, 2)                               A(1, 3)
    0       A(2, 2) - A(1, 2) * A(2, 1) / A(1, 1) A(2, 3) - A(1, 3) * A(2, 1) / A(1, 1)
    A(3, 1) A(3, 2)                               A(3, 3)
  ]
  
  
  B = [
    B(1)
    B(2)
    B(3) - B(1) * A(3, 1) / A(1, 1)
  ]
  
  A = [
    A(1, 1) A(1, 2)                               A(1, 3)
    0       A(2, 2)                               A(2, 3)
    0       A(3, 2) - A(1, 2) * A(3, 1) / A(1, 1) A(3, 3) - A(1, 3) * A(3, 1) / A(1, 1)
  ]
  
  B = [
    B(1)
    B(2)
    B(3) - B(2) * A(3, 2) / A(2, 2)
  ]
  
  A = [
    A(1, 1) A(1, 2) A(1, 3)
    0       A(2, 2) A(2, 3)
    0       0       A(3, 3) - A(1, 3) * A(3, 2) / A(2, 2)
  ]
  
  X3 = B(3) / A(3, 3)
  X2 = (B(2) - A(2, 3) * X3) / A(2, 2)
  X1 = (B(1) - A(1, 2) * X2 - A(1, 3) * X3) / A(1, 1)
  
  X = [X1, X2, X3]
endfunction