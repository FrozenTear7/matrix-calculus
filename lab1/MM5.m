function [C] = MM5(A,B)
  P1 = (A(1, 1) + A(2, 2)) * (B(1, 1) + B(2, 2));
  P2 = B(1, 1) * (A(2, 1) + A(2, 2));
  P3 = A(1, 1) * (B(1, 2) - B(2, 2));
  P4 = A(2, 2) * (B(2, 1) - B(1, 1));
  P5 = B(2, 2) * (A(1, 1) + A(1, 2));
  P6 = (A(2, 1) - A(1, 1)) * (B(1, 1) + B(1, 2));
  P7 = (A(1, 2) - A(2, 2)) * (B(2, 1) + B(2, 2));
  
  C = [
    P1 + P4 - P5 + P7 P3 + P5;
    P2 + P4 P1 - P2 + P3 + P6
  ];
endfunction